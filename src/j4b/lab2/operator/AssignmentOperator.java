package j4b.lab2.operator;

/**
 * 
 * Simple assignment operator
 *
 */
class AssignmentOperator {
	
	public static void main(String[] args) {
		
		int number1, number2;
		
		number1 = 5;
		System.out.println(number1);
				
		number2 = number1;
		System.out.println(number2);
	}
	
}
